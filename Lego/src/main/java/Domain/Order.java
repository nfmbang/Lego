package Domain;

/**
 *
 * @author nille
 */
public class Order {

    private int height;
    private int width;
    private int length;

    public Order(int length, int width, int height) {
        this.height = height;
        this.width = width;
        this.length = length;
    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

}
